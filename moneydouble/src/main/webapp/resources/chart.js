function plotAxisInterval() {
	this.cfg.axes.xaxis.tickOptions = {
            formatString : '%d'
        };
    this.cfg.axes.xaxis.tickInterval = 1;
    this.cfg.axes.xaxis.min = 0;
    this.cfg.axes.xaxis.showMinorTicks = false;
    this.cfg.axes.xaxis.drawMinorGridlines = false;
    this.cfg.axes.xaxis.drawMinorTickMarks = false;
}